import { Island, WindDirection, Accessibility, BeachType, Beach } from '../types';

/**
 * Helper to create a beach object in a compact way.
 * This ensures we can fit hundreds of beaches without code bloat.
 */
const b = (id: number, name: string, lat: number, lon: number, prot: WindDirection[], type: BeachType = 'sandy'): Beach => {
  const isDeep = id % 5 === 0;
  return {
    id, rating: 4.1 + (id % 9) / 10,
    name: { en: name, gr: name, fr: name, de: name, it: name },
    description: { 
      en: 'A wonderful coastal destination.', 
      gr: 'Ένας υπέροχος παραθαλάσσιος προορισμός.', 
      fr: 'Une magnifique destination côtière.', 
      de: 'Ein wunderbares Küstenziel.', 
      it: 'Una meravigliosa destinazione costiera.' 
    },
    protectedFrom: prot, beachType: type, accessibility: Accessibility.EASY,
    amenities: { 
      organized: id % 4 === 0, 
      naturalShade: id % 3 === 0, 
      taverna: true,
      beachBar: id % 2 === 0,
      sunbeds: id % 4 === 0,
      restaurant: id % 6 === 0,
      parking: true
    },
    characteristics: { deepWaters: isDeep, shallowWaters: !isDeep },
    waterDepth: isDeep ? 'deep' : 'shallow',
    activities: {
      snorkeling: id % 3 === 0,
      surfing: id % 10 === 0
    },
    environment: {
      quiet: id % 7 === 0,
      remote: id % 8 === 0,
      familyFriendly: id % 4 !== 0
    },
    popularityScore: 50 + (id % 50),
    coordinates: { lat, lon }
  };
};

const N_ALL = [WindDirection.N, WindDirection.NE, WindDirection.NW];
const S_ALL = [WindDirection.S, WindDirection.SE, WindDirection.SW];
const E_ALL = [WindDirection.E, WindDirection.NE, WindDirection.SE];
const W_ALL = [WindDirection.W, WindDirection.NW, WindDirection.SW];

// Helper to determine protection based on location in Euboea
const getEuboeaProt = (lon: number) => {
  return lon > 23.9 ? [...S_ALL, ...W_ALL] : [...N_ALL, ...E_ALL];
};

// Helper for Halkidiki protection
const getHalkidikiProt = (lat: number, lon: number) => {
  if (lat > 40.5) return [...S_ALL]; // Northern bays
  if (lon < 23.4) return [...E_ALL]; // Kassandra West
  if (lon < 23.65 && lon > 23.4) return [...W_ALL]; // Kassandra East
  if (lon < 23.85 && lon > 23.65) return [...E_ALL]; // Sithonia West
  return [...W_ALL]; // Sithonia East / Athos
};

// Helper for Crete protection
const getCreteProt = (lat: number) => {
  return lat > 35.4 ? S_ALL : N_ALL; // North coast protected from South, South coast from North
};

// Helper for Epirus protection
const getEpirusProt = (lat: number, lon: number) => {
    return [...E_ALL, ...N_ALL]; // Protected by the mainland mountains
};

// Helper for Ionian protection (Lefkada, Kefalonia)
const getIonianProt = (lat: number, lon: number) => {
    if (lon < 20.6) return [...E_ALL, ...N_ALL];
    return [...W_ALL, ...N_ALL];
};

// Specific helpers for the new regions
const getKerkyraProt = (lon: number) => lon < 19.75 ? [...E_ALL, ...N_ALL] : [...W_ALL, ...N_ALL];
const getZakynthosProt = (lon: number) => lon < 20.8 ? [...E_ALL, ...N_ALL] : [...W_ALL, ...N_ALL];
const getChiosProt = (lat: number, lon: number) => lon < 26.0 ? [...E_ALL, ...N_ALL] : [...W_ALL, ...N_ALL];

